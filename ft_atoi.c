#include "libft.h"

int		ft_atoi(const char *s)
{
	int i;
	int r;

	i = 1;
	r = 0;
	while (*s == 32 || (*s >= 9 && *s <= 13))
		s++;
	if (*s == '-' || *s == '+')
	{
		if (*s == '-')
			i = -1;
		s++;
	}
	if (!(*s >= '0' && *s <= '9'))
		return (0);
	while (*s >= '0' && *s <= '9')
	{
		r = (r * 10) + (*s - '0');
		s++;
	}
	return (r * i);
}
