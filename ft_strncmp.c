#include "libft.h"

int		ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t i;

	i = 0;
	while (*s1++ == *s2++ && n--)
		if (!n)
			return (0);
	return ((char *)s1 - (char *)(s2));
}
